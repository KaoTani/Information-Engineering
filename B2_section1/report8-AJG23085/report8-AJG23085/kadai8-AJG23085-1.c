double wa(double a, double b){
  double c;
  c = a + b;
  
  return c;
}

double sa(double a, double b){
  double c;
  c = a - b;

  return c;
}

double seki(double a, double b){
  double c;
  c = a * b;

  return c;
}

double syou(double a, double b){
  double c;
  c = a / b;

  return c;
}